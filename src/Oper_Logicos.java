public class Oper_Logicos {
    public static void main(String[] args) {

        boolean a =true, b= false;

        //AND (Y)
        System.out.println(a & b);

        //OR (O)
        System.out.println(a || b);

        //NOT (NO)
        System.out.println(!a);

        //XOR LOGICO
        System.out.println(a ^ b);

    }
}
